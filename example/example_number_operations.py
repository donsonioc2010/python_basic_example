#한쪽만 소수형이더라도 결과값은 소수형이 나온다 나눗셈을 제외하고

# 덧셈
print("+")
print(4+7)
print(4.0+7.0)

# 뺄셈
print("-")
print(2-4)
print(2.0-4.0)

# 곱셈
print("*")
print(5*3)
print(5.0*3.0)

# 나머지
print("%")
print(7%3)
print(7.0%3.0)

# 거듭제곱
print("**")
print(2**3) #=> 2 * 2 * 2
print(2.0**3.0) #=> 2 * 2 * 2

# 나눗셈 (항상 소수형이 나옴 정수형은 여기서 형변환을 하면 됨)
print("/")
print(7/2)
print(6/2)
print(7.0/2)
print(6.0/2.0)

# floor division(버림 나눗셈) : 나눔에서 소수를 버리고 정수값만 가져옴
# 둘중한개가 소수면 결과값도 소수로 나오긴 함
print("//")
print(8//3)
print(8.0//3)

# round (반올림)
print("round")
print(round(3.1414926535))
print(round(3.1414926535,2))

